#include <bits/stdc++.h>

using namespace std;

int main() 
{
    int x,y;
    cin>>x >>y;
    cout<<"Before swapping" <<"/n";
    cout<<"Valua of x:" <<x <<"/n";
    cout<<"Value of y:" <<y <<"/n";
    swap(x,y);
    cout<<"After the swapping" <<"/n";
    cout<<"Value of x:" <<x <<"/n";
    cout<<"Value of y:" <<y <<"/n";
}